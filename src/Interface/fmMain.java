    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String idSV = listSinhVien.get(boxSinhVien.getSelectedItem().toString());
        int idDT = listDeTai.get(boxDeTai.getSelectedItem().toString());
        try {
            if (idSV.length() == 0) {
                JOptionPane.showMessageDialog(null, "Chọn giáo viên để sửa",
                        "Thông báo", 1);
            } else {
                if (JOptionPane.showConfirmDialog(null, "Bạn có muốn sửa " + idSV + " thuộc "+ idDT +" ? ", "Thong bao", 2) == 0) {
                    phanCong.EditData(listSinhVien.get(boxSinhVien.getSelectedItem().toString()), listDeTai.get(boxDeTai.getSelectedItem().toString()),txtMaGVHD.getText(), txtMaGVPB.getText(),(Integer)txtDiemGVHD.getValue(),(Integer)txtDiemGVPB.getValue(),textNXGVHD.getText(),textNXGVPB.getText());
                }
            }
            clear();
            ShowData();
            setNull();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "sua thất bại", "Thông báo", 1);
        }     

    }//GEN-LAST:event_btnEditActionPerformed

