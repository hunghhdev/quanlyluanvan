    public void showSinhVien() throws SQLException {
        listSinhVien = new HashMap<>();
        ResultSet resultSet = sinhVien.ShowSinhVien();
        try {
            while (resultSet.next()) {
                listSinhVien.put(resultSet.getString(2), resultSet.getString(1));
            }
            for (String s : listSinhVien.keySet()) {
                boxSinhVien.addItem(s);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    public void showDeTai() throws SQLException {
        listDeTai = new HashMap<>();
        ResultSet resultSet = deTai.ShowDeTai();
        try {
            while (resultSet.next()) {
                listDeTai.put(resultSet.getString(2), resultSet.getInt(1));
            }
            for (String s : listDeTai.keySet()) {
                boxDeTai.addItem(s);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void ShowData() throws SQLException {
        ResultSet result = phanCong.ShowPhanCong();
        try {
            while (result.next()) {
                String rows[] = new String[8];
                rows[0] = result.getString(1);
                rows[1] = result.getString(2);
                rows[2] = result.getString(3);
                rows[3] = result.getString(4);
                rows[4] = result.getString(5);
                rows[5] = result.getString(6);
                rows[6] = result.getString(7);
                rows[7] = result.getString(8);
                tableModel.addRow(rows);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

