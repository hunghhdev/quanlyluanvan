    private void tableDanhSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDanhSachMouseClicked
        // TODO add your handling code here:
        int i = tableDanhSach.getSelectedRow();
        for (Map.Entry<String, String> entry : listSinhVien.entrySet()) {
            if (entry.getValue().equals(tableDanhSach.getValueAt(i, 0))) {
                this.boxSinhVien.setSelectedItem(entry.getKey());
            }
        }
        for (Map.Entry<String, Integer > entry : listDeTai.entrySet()) {
            if (entry.getValue() == Integer.parseInt(tableDanhSach.getValueAt(i, 1).toString())) {
                this.boxDeTai.setSelectedItem(entry.getKey());
            }
        }
        this.txtMaGVHD.setText(tableDanhSach.getValueAt(i, 2).toString());
        this.txtMaGVPB.setText(tableDanhSach.getValueAt(i, 3).toString());
        this.txtDiemGVHD.setValue(Integer.parseInt(tableDanhSach.getValueAt(i, 4).toString()));
        this.txtDiemGVPB.setValue(Integer.parseInt(tableDanhSach.getValueAt(i, 5).toString()));
        this.textNXGVHD.setText(tableDanhSach.getValueAt(i, 6).toString());
        this.textNXGVPB.setText(tableDanhSach.getValueAt(i, 7).toString());
    }//GEN-LAST:event_tableDanhSachMouseClicked

